function rand_color() {
    return '#' . str_pad(dechex(mt_rand(0, 0xFFFFFF)), 6, '0', STR_PAD_LEFT);
}

function hashingPassword($u, $p){
	$salt = "EPADIBAKEUDA@2023";
	$hashing = md5($u);
	$salting = sha1($salt.$hashing.$u.$salt.$p.$salt);
	return $salting;
}
